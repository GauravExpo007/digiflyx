<script>
    document.addEventListener("DOMContentLoaded", function() {
        const slider = document.getElementById('seoServiceSlider');
        const next = document.getElementById('seoNext');
        const prev = document.getElementById('seoPrev');
        let autoScroll;

        function scrollNext() {
            slider.scrollBy({
                left: 320,
                behavior: 'smooth'
            });
        }

        function scrollPrev() {
            slider.scrollBy({
                left: -320,
                behavior: 'smooth'
            });
        }

        function startAutoScroll() {
            autoScroll = setInterval(scrollNext, 3000);
        }

        function stopAutoScroll() {
            clearInterval(autoScroll);
        }

        next.addEventListener('click', () => {
            scrollNext();
            stopAutoScroll();
            startAutoScroll();
        });

        prev.addEventListener('click', () => {
            scrollPrev();
            stopAutoScroll();
            startAutoScroll();
        });

        startAutoScroll();
    });
</script>
